int StringToInt(char* String) 
{ 
	int nRetCode; 

	nRetCode = CT[*String]; 
	nRetCode = (nRetCode * 16) + CT[*(String + 1)]; 
	return nRetCode; 
} 

bool GetMacAddr(char* szMacAddr_String, char* MacAddr) 
{ 
	int i; 
	char szTemp[2]; 
	for (i = 0; i < 6; i++) 
	{ 
		//szTemp = {0}; 
		szTemp[0] = *(szMacAddr_String + (2 * i)); 
		szTemp[1] = *(szMacAddr_String + (2 * i) + 1); 
		*(MacAddr + i) = StringToInt(szTemp); 
		if (*(MacAddr + i) > 0xFF) 
			return false; 
	} 
	return true; 
}